package org.ashsethu.model;

public class Response {
    ExternalImages external_images;
    InternalImages internal_images;
    ExternalLinks external_links;
    InternalLinks internal_links;

    public ExternalImages getExternal_images() {
        return external_images;
    }

    public void setExternal_images(ExternalImages external_images) {
        this.external_images = external_images;
    }

    public InternalImages getInternal_images() {
        return internal_images;
    }

    public void setInternal_images(InternalImages internal_images) {
        this.internal_images = internal_images;
    }

    public ExternalLinks getExternal_links() {
        return external_links;
    }

    public void setExternal_links(ExternalLinks external_links) {
        this.external_links = external_links;
    }

    public InternalLinks getInternal_links() {
        return internal_links;
    }

    public void setInternal_links(InternalLinks internal_links) {
        this.internal_links = internal_links;
    }



}
